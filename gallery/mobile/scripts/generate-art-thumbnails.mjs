#!/usr/bin/env node

import { execFile } from 'node:child_process';
import { access, lstat, mkdtemp, readFile, readdir, rename, rm, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { promisify } from 'node:util';
import { fileURLToPath } from 'node:url';
import { artWorks } from '../../art/works.js';
import { ART_WALLS } from '../tour-manifest.js';

const execFileAsync = promisify(execFile);
const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const MOBILE_DIR = path.dirname(SCRIPT_DIR);
const GALLERY_DIR = path.dirname(MOBILE_DIR);
const OUTPUT_DIR = path.join(MOBILE_DIR, 'art-thumbs');
const MARKER = '.generated-by-generate-art-thumbnails';
const MARKER_TEXT = 'Generated by gallery/mobile/scripts/generate-art-thumbnails.mjs\n';
const THUMB_SIZE = 360;
const WALL_PREVIEW_SIZE = 1440;
const MAX_THUMB_BYTES = 160 * 1024;
const MAX_WALL_PREVIEW_BYTES = 320 * 1024;
const MAGICK = process.env.MAGICK_PATH || 'magick';
const magickBasename = path.basename(MAGICK).toLowerCase().replace(/\.exe$/, '');
const magickWrapper = magickBasename === 'magick';
const IDENTIFY = process.env.IDENTIFY_PATH || (magickWrapper
  ? MAGICK
  : path.join(path.dirname(MAGICK), 'identify'));
const COMMAND_ENV = { ...process.env, LANG: 'C', LC_ALL: 'C', TZ: 'UTC', MAGICK_THREAD_LIMIT: '1' };

async function run(command, args) {
  return execFileAsync(command, args, {
    encoding: 'utf8',
    env: COMMAND_ENV,
    maxBuffer: 1024 * 1024,
    timeout: 30_000
  });
}

const runMagick = (args) => run(MAGICK, args);
const identify = (target) => run(
  IDENTIFY,
  magickWrapper ? ['identify', '-format', '%w %h', target] : ['-format', '%w %h', target]
);

async function verify(directory) {
  const expected = new Map([
    ...artWorks.map((work) => [
      `${work.id}.webp`,
      { maxSize: THUMB_SIZE, maxBytes: MAX_THUMB_BYTES }
    ]),
    ...Object.values(ART_WALLS).map((wall) => [
      `wall-${wall.id}.webp`,
      { maxSize: WALL_PREVIEW_SIZE, maxBytes: MAX_WALL_PREVIEW_BYTES }
    ])
  ]);
  const files = (await readdir(directory)).filter((file) => file.endsWith('.webp'));
  if (files.length !== expected.size || files.some((file) => !expected.has(file))) {
    throw new Error(`Expected exactly ${expected.size} artwork thumbnails and wall previews, found ${files.length}.`);
  }
  if (await readFile(path.join(directory, MARKER), 'utf8') !== MARKER_TEXT) {
    throw new Error('Artwork thumbnail marker is missing or invalid.');
  }
  let totalBytes = 0;
  for (const file of files) {
    const limits = expected.get(file);
    const target = path.join(directory, file);
    const fileStat = await stat(target);
    if (fileStat.size > limits.maxBytes) throw new Error(`${file} exceeds its byte limit.`);
    totalBytes += fileStat.size;
    const { stdout } = await identify(target);
    const [width, height] = stdout.trim().split(/\s+/).map(Number);
    if (!width || !height || width > limits.maxSize || height > limits.maxSize) {
      throw new Error(`${file} has invalid dimensions ${width}x${height}.`);
    }
  }
  console.log(`Verified ${artWorks.length} artwork thumbnails and ${Object.keys(ART_WALLS).length} wall previews, ${totalBytes} bytes.`);
}

async function removeMarked(directory, allowedParent, prefix) {
  if (path.dirname(directory) !== allowedParent || !path.basename(directory).startsWith(prefix)) {
    throw new Error(`Refusing unsafe cleanup: ${directory}`);
  }
  let item;
  try {
    item = await lstat(directory);
  } catch (error) {
    if (error.code === 'ENOENT') return;
    throw error;
  }
  if (!item.isDirectory() || item.isSymbolicLink()) {
    throw new Error(`Refusing unsafe cleanup: ${directory}`);
  }
  if ((await readFile(path.join(directory, MARKER), 'utf8').catch(() => '')) !== MARKER_TEXT) {
    throw new Error(`Refusing to remove unmarked directory: ${directory}`);
  }
  await rm(directory, { recursive: true });
}

async function replaceOutput(buildDirectory) {
  let current;
  try {
    current = await lstat(OUTPUT_DIR);
  } catch (error) {
    if (error.code !== 'ENOENT') throw error;
  }
  if (!current) {
    await rename(buildDirectory, OUTPUT_DIR);
    return;
  }
  if (!current.isDirectory() || current.isSymbolicLink()) {
    throw new Error(`Refusing to replace non-directory ${OUTPUT_DIR}.`);
  }
  if ((await readFile(path.join(OUTPUT_DIR, MARKER), 'utf8').catch(() => '')) !== MARKER_TEXT) {
    throw new Error(`Refusing to replace unmarked directory ${OUTPUT_DIR}.`);
  }
  const backupDirectory = path.join(MOBILE_DIR, `.art-thumbs-backup-${process.pid}`);
  await rename(OUTPUT_DIR, backupDirectory);
  try {
    await rename(buildDirectory, OUTPUT_DIR);
  } catch (error) {
    await rename(backupDirectory, OUTPUT_DIR);
    throw error;
  }
  await removeMarked(backupDirectory, MOBILE_DIR, '.art-thumbs-backup-');
}

if (process.argv.includes('--verify')) {
  await verify(OUTPUT_DIR);
  process.exit(0);
}

const ids = new Set();
for (const work of artWorks) {
  if (ids.has(work.id)) throw new Error(`Duplicate artwork ID: ${work.id}`);
  ids.add(work.id);
}

const buildDirectory = await mkdtemp(path.join(MOBILE_DIR, '.art-thumbs-build-'));
try {
  await writeFile(path.join(buildDirectory, MARKER), MARKER_TEXT);
  for (const work of artWorks) {
    const source = path.resolve(GALLERY_DIR, work.image);
    await access(source);
    await runMagick([
      source,
      '-auto-orient',
      '-thumbnail', `${THUMB_SIZE}x${THUMB_SIZE}>`,
      '-strip',
      '-define', 'webp:method=6',
      '-quality', '76',
      path.join(buildDirectory, `${work.id}.webp`)
    ]);
  }
  for (const wall of Object.values(ART_WALLS)) {
    const source = path.resolve(GALLERY_DIR, wall.image);
    await access(source);
    await runMagick([
      source,
      '-auto-orient',
      '-thumbnail', `${WALL_PREVIEW_SIZE}x${WALL_PREVIEW_SIZE}>`,
      '-strip',
      '-define', 'webp:method=6',
      '-quality', '76',
      path.join(buildDirectory, `wall-${wall.id}.webp`)
    ]);
  }
  await verify(buildDirectory);
  await replaceOutput(buildDirectory);
} finally {
  await removeMarked(buildDirectory, MOBILE_DIR, '.art-thumbs-build-').catch(() => {});
}
