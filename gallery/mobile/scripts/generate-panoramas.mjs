class CdpClient {
  constructor(socket) {
    this.socket = socket;
    this.nextId = 1;
    this.pending = new Map();
    this.listeners = new Map();
    socket.addEventListener('message', ({ data }) => this.receive(data));
    socket.addEventListener('close', () => {
      for (const { reject } of this.pending.values()) reject(new Error('Chrome DevTools connection closed.'));
      this.pending.clear();
    });
  }

  static async connect(url) {
    if (typeof WebSocket === 'undefined') throw new Error('Node.js 22 or newer is required (global WebSocket missing).');
    const socket = new WebSocket(url);
    await new Promise((resolve, reject) => {
      socket.addEventListener('open', resolve, { once: true });
      socket.addEventListener('error', () => reject(new Error('Could not connect to Chrome DevTools.')), { once: true });
    });
    return new CdpClient(socket);
  }

  async receive(data) {
    const text = typeof data === 'string' ? data : Buffer.from(await data.arrayBuffer?.() || data).toString();
    const message = JSON.parse(text);
    if (message.id) {
      const pending = this.pending.get(message.id);
      if (!pending) return;
      this.pending.delete(message.id);
      clearTimeout(pending.timer);
      if (message.error) pending.reject(new Error(`${pending.method}: ${message.error.message}`));
      else pending.resolve(message.result);
      return;
    }
    for (const listener of this.listeners.get(message.method) || []) listener(message.params);
  }

  on(method, listener) {
    if (!this.listeners.has(method)) this.listeners.set(method, []);
    this.listeners.get(method).push(listener);
  }

  send(method, params = {}, timeout = 120_000) {
    const id = this.nextId++;
    return new Promise((resolve, reject) => {
      const timer = setTimeout(() => { this.pending.delete(id); reject(new Error(`${method} timed out.`)); }, timeout);
      this.pending.set(id, { method, resolve, reject, timer });
      this.socket.send(JSON.stringify({ id, method, params }));
    });
  }

  waitFor(method, timeout = 60_000) {
    return new Promise((resolve, reject) => {
      const listener = (params) => { clearTimeout(timer); resolve(params); };
      const timer = setTimeout(() => reject(new Error(`${method} timed out.`)), timeout);
      this.on(method, listener);
    });
  }

  close() { this.socket.close(); }
}

async function evaluate(client, expression, timeout = 180_000) {
  const response = await client.send('Runtime.evaluate', {
    expression, awaitPromise: true, returnByValue: true
  }, timeout);
  if (response.exceptionDetails) {
    throw new Error(response.exceptionDetails.exception?.description || response.exceptionDetails.text);
  }
  return response.result.value;
}

function installFailureTracking(client) {
  const state = {
    requests: new Map(), requestTypes: new Map(),
    responses: new Map(), receivedBytes: new Map(), consoleErrors: [],
    failedRequests: [], badResponses: [], mediaRequests: [], mediaBytes: 0
  };
  client.on('Network.requestWillBeSent', ({ requestId, request, type }) => {
    state.requests.set(requestId, request.url);
    state.requestTypes.set(requestId, type);
    if (MEDIA_RE.test(request.url)) state.mediaRequests.push(request.url);
  });
  client.on('Network.loadingFinished', ({ requestId, encodedDataLength }) => {
    const url = state.requests.get(requestId) || '';
    if (MEDIA_RE.test(url)) state.mediaBytes += encodedDataLength || 0;
  });
  client.on('Network.loadingFailed', ({ requestId, errorText, blockedReason, canceled }) => {
    state.failedRequests.push({
      requestId,
      url: state.requests.get(requestId) || requestId,
      detail: blockedReason || errorText,
      type: state.requestTypes.get(requestId),
      canceled: canceled === true
    });
  });
  client.on('Network.dataReceived', ({ requestId, dataLength }) => {
    state.receivedBytes.set(requestId, (state.receivedBytes.get(requestId) || 0) + dataLength);
  });
  client.on('Network.responseReceived', ({ requestId, response, type }) => {
    const contentLengthEntry = Object.entries(response.headers || {})
      .find(([name]) => name.toLowerCase() === 'content-length');
    state.responses.set(requestId, {
      status: response.status,
      mimeType: response.mimeType,
      protocol: response.protocol,
      contentLength: Number(contentLengthEntry?.[1]),
      type
    });
    if (response.status >= 400) state.badResponses.push(`${response.status} ${response.url}`);
  });
  client.on('Runtime.consoleAPICalled', ({ type, args }) => {
    if (type === 'error' || type === 'assert') {
      state.consoleErrors.push(args.map((arg) => arg.value ?? arg.description ?? arg.type).join(' '));
    }
  });
  client.on('Runtime.exceptionThrown', ({ exceptionDetails }) => {
    state.consoleErrors.push(exceptionDetails.exception?.description || exceptionDetails.text);
  });
  client.on('Log.entryAdded', ({ entry }) => {
    if (entry.level === 'error') state.consoleErrors.push(entry.text);
  });
  return state;
}

function isCompleteCanceledFetch(state, failure) {
  const response = state.responses.get(failure.requestId);
  const receivedBytes = state.receivedBytes.get(failure.requestId) || 0;
  return failure.detail === 'net::ERR_ABORTED'
    && failure.canceled
    && failure.type === 'Fetch'
    && response?.status >= 200
    && response.status < 300
    && Number.isFinite(response.contentLength)
    && response.contentLength > 0
    && receivedBytes === response.contentLength;
}

function assertClean(state, server) {
  const problems = [];
  const unresolvedFailures = state.failedRequests.filter((failure) => !isCompleteCanceledFetch(state, failure));
  if (state.consoleErrors.length) problems.push(`console errors:\n${state.consoleErrors.join('\n')}`);
  if (unresolvedFailures.length) {
    problems.push(`failed requests:\n${unresolvedFailures.map(({ requestId, url, detail }) => {
      const response = state.responses.get(requestId);
      const responseDetail = response
        ? ` after HTTP ${response.status} ${response.mimeType} via ${response.protocol}`
        : ' before any response';
      return `${url}: ${detail}${responseDetail}`;
    }).join('\n')}`);
  }
  if (state.badResponses.length) problems.push(`HTTP errors:\n${state.badResponses.join('\n')}`);
  if (server.serverIssues.length) problems.push(`static-server errors:\n${server.serverIssues.join('\n')}`);
  if (state.mediaRequests.length || server.mediaRequests.length || state.mediaBytes) {
    problems.push(`media requested/transferred (${state.mediaBytes} bytes):\n${[...state.mediaRequests, ...server.mediaRequests].join('\n')}`);
  }
  if (problems.length) throw new Error(`Capture page was not clean:\n\n${problems.join('\n\n')}`);
}

