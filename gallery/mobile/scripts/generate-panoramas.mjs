function parseArgs(argv) {
  const result = { verify: false, help: false, nodes: null };
  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index];
    if (argument === '--verify') result.verify = true;
    else if (argument === '--help' || argument === '-h') result.help = true;
    else if (argument === '--nodes' || argument.startsWith('--nodes=')) {
      const value = argument === '--nodes' ? argv[++index] : argument.slice('--nodes='.length);
      if (!value) throw new Error('--nodes requires a comma-separated list.');
      const requested = value.split(',').map((item) => item.trim()).filter(Boolean);
      if (requested.length === 0 || new Set(requested).size !== requested.length) {
        throw new Error('--nodes must contain unique node IDs.');
      }
      const unknown = requested.filter((id) => !EXPECTED_NODES.includes(id));
      if (unknown.length) throw new Error(`Unknown node ID(s): ${unknown.join(', ')}`);
      const selected = new Set(requested);
      result.nodes = EXPECTED_NODES.filter((id) => selected.has(id));
    } else throw new Error(`Unknown argument: ${argument}\n\n${usage()}`);
  }
  if (result.verify && result.nodes) {
    throw new Error('--verify checks the complete panorama set and cannot be combined with --nodes.');
  }
  return result;
}

function sha256(buffer) {
  return createHash('sha256').update(buffer).digest('hex');
}

function relativeAsset(root, file) {
  return path.relative(root, file).split(path.sep).join('/');
}

async function probe(command, args) {
  const { stdout, stderr } = await execFileAsync(command, args, {
    encoding: 'utf8', env: COMMAND_ENV, maxBuffer: 1024 * 1024, timeout: 15_000
  });
  return `${stdout}\n${stderr}`.trim().split(/\r?\n/).find(Boolean) || 'unknown';
}

async function findChrome() {
  const candidates = [
    process.env.CHROME_PATH,
    '/Applications/Google Chrome.app/Contents/MacOS/Google Chrome',
    '/opt/google/chrome/chrome', '/usr/bin/google-chrome', '/usr/bin/google-chrome-stable',
    process.env.PROGRAMFILES && path.join(process.env.PROGRAMFILES, 'Google/Chrome/Application/chrome.exe')
  ].filter(Boolean);
  for (const candidate of candidates) {
    try {
      await access(candidate);
      const version = await probe(candidate, ['--version']);
      return { command: candidate, version };
    } catch {}
  }
  throw new Error('Google Chrome was not found. Set CHROME_PATH to its executable.');
}

async function findCommand(envName, candidates, versionArgs) {
  const attempted = [process.env[envName], ...candidates].filter(Boolean);
  for (const command of attempted) {
    try { return { command, version: await probe(command, versionArgs) }; } catch {}
  }
  throw new Error(`${envName.replace('_PATH', '')} was not found. Set ${envName}.`);
}

async function run(command, args, timeout = 120_000) {
  try {
    return await execFileAsync(command, args, {
      encoding: 'utf8', env: COMMAND_ENV, maxBuffer: 8 * 1024 * 1024, timeout
    });
  } catch (error) {
    const detail = String(error.stderr || error.stdout || error.message).trim();
    throw new Error(`${path.basename(command)} failed: ${detail}`);
  }
}

function startStaticServer() {
  const serverIssues = [];
  const mediaRequests = [];
  const server = createServer(async (request, response) => {
    try {
      if (!['GET', 'HEAD'].includes(request.method)) {
        response.writeHead(405).end();
        return;
      }
      const url = new URL(request.url, 'http://127.0.0.1');
      if (MEDIA_RE.test(url.pathname)) {
        mediaRequests.push(url.pathname);
        response.writeHead(451, { 'Cache-Control': 'no-store' }).end();
        return;
      }
      if (url.pathname === '/favicon.ico') {
        response.writeHead(204, { 'Cache-Control': 'no-store' }).end();
        return;
      }
      let decoded;
      try { decoded = decodeURIComponent(url.pathname); } catch { response.writeHead(400).end(); return; }
      let file = path.resolve(SITE_ROOT, `.${decoded}`);
      if (file !== SITE_ROOT && !file.startsWith(`${SITE_ROOT}${path.sep}`)) {
        response.writeHead(403).end();
        return;
      }
      let fileStat;
      try {
        fileStat = await stat(file);
        if (fileStat.isDirectory()) {
          file = path.join(file, 'index.html');
          fileStat = await stat(file);
        }
      } catch {
        serverIssues.push(`404 ${url.pathname}`);
        response.writeHead(404, { 'Cache-Control': 'no-store' }).end('Not found');
        return;
      }
      if (!fileStat.isFile()) {
        serverIssues.push(`404 ${url.pathname}`);
        response.writeHead(404).end();
        return;
      }
      response.writeHead(200, {
        'Cache-Control': 'no-store', 'Content-Length': fileStat.size,
        'Content-Type': MIME.get(path.extname(file).toLowerCase()) || 'application/octet-stream'
      });
      if (request.method === 'HEAD') response.end();
      else createReadStream(file).on('error', (error) => response.destroy(error)).pipe(response);
    } catch (error) {
      serverIssues.push(error.message);
      if (!response.headersSent) response.writeHead(500);
      response.end();
    }
  });
  return new Promise((resolve, reject) => {
    server.once('error', reject);
    server.listen(0, '127.0.0.1', () => {
      const { port } = server.address();
      resolve({
        origin: `http://127.0.0.1:${port}`, mediaRequests, serverIssues,
        close: () => new Promise((done, fail) => server.close((error) => error ? fail(error) : done()))
      });
    });
  });
}

function waitForDevTools(chrome, stderrLog) {
  return new Promise((resolve, reject) => {
    const timer = setTimeout(() => reject(new Error(`Chrome DevTools did not start.\n${stderrLog.value}`)), 30_000);
    const onData = (chunk) => {
      stderrLog.value = `${stderrLog.value}${chunk}`.slice(-16_384);
      const match = stderrLog.value.match(/DevTools listening on (ws:\/\/[^\s]+)/);
      if (match) { clearTimeout(timer); resolve(match[1]); }
    };
    chrome.stderr.on('data', onData);
    chrome.once('error', (error) => { clearTimeout(timer); reject(error); });
    chrome.once('exit', (code) => { clearTimeout(timer); reject(new Error(`Chrome exited early (${code}).`)); });
  });
}

