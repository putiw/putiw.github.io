function decodePng(dataUrl, nodeId, face) {
  const match = /^data:image\/png;base64,([A-Za-z0-9+/=]+)$/.exec(dataUrl || '');
  if (!match) throw new Error(`${nodeId}/${face} did not return a PNG data URL.`);
  const buffer = Buffer.from(match[1], 'base64');
  const signature = '89504e470d0a1a0a';
  if (buffer.length < 24 || buffer.subarray(0, 8).toString('hex') !== signature) {
    throw new Error(`${nodeId}/${face} returned an invalid PNG.`);
  }
  const width = buffer.readUInt32BE(16);
  const height = buffer.readUInt32BE(20);
  if (width !== SOURCE_SIZE || height !== SOURCE_SIZE) {
    throw new Error(`${nodeId}/${face} is ${width}x${height}; expected ${SOURCE_SIZE}x${SOURCE_SIZE}.`);
  }
  return buffer;
}

function webpDimensions(buffer) {
  if (buffer.length < 30 || buffer.toString('ascii', 0, 4) !== 'RIFF' || buffer.toString('ascii', 8, 12) !== 'WEBP') {
    throw new Error('Invalid WebP file.');
  }
  let offset = 12;
  while (offset + 8 <= buffer.length) {
    const type = buffer.toString('ascii', offset, offset + 4);
    const size = buffer.readUInt32LE(offset + 4);
    const data = offset + 8;
    if (type === 'VP8X') return [1 + buffer.readUIntLE(data + 4, 3), 1 + buffer.readUIntLE(data + 7, 3)];
    if (type === 'VP8 ' && buffer.toString('hex', data + 3, data + 6) === '9d012a') {
      return [buffer.readUInt16LE(data + 6) & 0x3fff, buffer.readUInt16LE(data + 8) & 0x3fff];
    }
    if (type === 'VP8L' && buffer[data] === 0x2f) {
      return [1 + buffer[data + 1] + ((buffer[data + 2] & 0x3f) << 8),
        1 + (buffer[data + 2] >> 6) + (buffer[data + 3] << 2) + ((buffer[data + 4] & 0x0f) << 10)];
    }
    offset = data + size + (size & 1);
  }
  throw new Error('WebP dimensions could not be read.');
}

async function assetRecord(root, file, level, face, row, column, expectedWidth, expectedHeight = expectedWidth) {
  const buffer = await readFile(file);
  const [width, height] = webpDimensions(buffer);
  if (width !== expectedWidth || height !== expectedHeight) {
    throw new Error(`${file} is ${width}x${height}; expected ${expectedWidth}x${expectedHeight}.`);
  }
  return { path: relativeAsset(root, file), level, face, row, column, width, height, bytes: buffer.length, sha256: sha256(buffer) };
}

async function encodeWebp(cwebp, source, destination, quality) {
  await mkdir(path.dirname(destination), { recursive: true });
  await run(cwebp, [
    '-quiet', '-q', String(quality), '-m', '6', '-segments', '4', '-sns', '50',
    '-f', '0', '-sharpness', '0', '-sharp_yuv', '-metadata', 'none', '-exact',
    source, '-o', destination
  ]);
}

async function buildFace({ nodeId, face, png, buildDir, workDir, magick, cwebp }) {
  const faceWork = path.join(workDir, nodeId, face);
  await mkdir(faceWork, { recursive: true });
  const source = path.join(faceWork, 'source.png');
  const basePng = path.join(faceWork, 'base.png');
  await writeFile(source, png);
  const imageStats = await run(magick, [
    source, '-colorspace', 'sRGB',
    '-format', '%[fx:(mean.r+mean.g+mean.b)/3] %[fx:standard_deviation]', 'info:'
  ]);
  const [mean, deviation] = imageStats.stdout.trim().split(/\s+/).map(Number);
  if (!Number.isFinite(mean) || !Number.isFinite(deviation) || (mean <= 0.002 && deviation <= 0.0001)) {
    throw new Error(`${nodeId}/${face} is blank.`);
  }
  await run(magick, [
    source, '-colorspace', 'sRGB', '-filter', 'Lanczos',
    '-resize', `${BASE_SIZE}x${BASE_SIZE}!`, '-strip',
    '-define', 'png:exclude-chunks=date,time', basePng
  ]);
  const records = [];
  const baseOut = path.join(buildDir, nodeId, 'base', `${face}.webp`);
  await encodeWebp(cwebp, basePng, baseOut, BASE_QUALITY);
  records.push(await assetRecord(buildDir, baseOut, 'base', face, 0, 0, BASE_SIZE));
  for (const level of REFINEMENT_LEVELS) {
    const grid = Math.ceil(level / TILE_SIZE);
    const tilePattern = path.join(faceWork, `${level}-tile-%d.png`);
    const resizeArgs = level === SOURCE_SIZE ? [] : ['-filter', 'Lanczos', '-resize', `${level}x${level}!`];
    await run(magick, [
      source, '-colorspace', 'sRGB', ...resizeArgs,
      '-crop', `${TILE_SIZE}x${TILE_SIZE}`, '+repage', '-strip', '+adjoin', tilePattern
    ]);
    for (let index = 0; index < grid * grid; index += 1) {
      const row = Math.floor(index / grid);
      const column = index % grid;
      const width = Math.min(TILE_SIZE, level - column * TILE_SIZE);
      const height = Math.min(TILE_SIZE, level - row * TILE_SIZE);
      const tileOut = path.join(buildDir, nodeId, String(level), face, `${row}-${column}.webp`);
      await encodeWebp(cwebp, path.join(faceWork, `${level}-tile-${index}.png`), tileOut, DETAIL_QUALITY);
      records.push(await assetRecord(buildDir, tileOut, String(level), face, row, column, width, height));
    }
  }
  return records;
}

async function listWebps(directory, root = directory) {
  const found = [];
  for (const entry of await readdir(directory, { withFileTypes: true })) {
    const child = path.join(directory, entry.name);
    if (entry.isDirectory()) found.push(...await listWebps(child, root));
    else if (entry.isFile() && entry.name.endsWith('.webp')) found.push(relativeAsset(root, child));
  }
  return found.sort();
}

function expectedNodeAssets(nodeId) {
  const expected = new Map();
  for (const face of FACES) {
    const basePath = `${nodeId}/base/${face}.webp`;
    expected.set(basePath, {
      path: basePath, level: 'base', face, row: 0, column: 0,
      width: BASE_SIZE, height: BASE_SIZE
    });
    for (const level of REFINEMENT_LEVELS) {
      const grid = Math.ceil(level / TILE_SIZE);
      for (let row = 0; row < grid; row += 1) {
        for (let column = 0; column < grid; column += 1) {
          const tilePath = `${nodeId}/${level}/${face}/${row}-${column}.webp`;
          expected.set(tilePath, {
            path: tilePath, level: String(level), face, row, column,
            width: Math.min(TILE_SIZE, level - column * TILE_SIZE),
            height: Math.min(TILE_SIZE, level - row * TILE_SIZE)
          });
        }
      }
    }
  }
  return expected;
}

async function verify(directory = OUTPUT_DIR, { expectedAssetVersion = ASSET_VERSION } = {}) {
  if ((await readFile(path.join(directory, MARKER), 'utf8')) !== MARKER_TEXT) throw new Error('Panorama marker is missing or invalid.');
  const manifest = JSON.parse(await readFile(path.join(directory, 'manifest.json'), 'utf8'));
  const expectedNodes = EXPECTED_NODES;
  if (JSON.stringify(manifest.nodes.map((node) => node.id)) !== JSON.stringify(expectedNodes)) throw new Error('Manifest node list is not the expected list.');
  const refinementLayoutsAreValid = REFINEMENT_LEVELS.every((level) => {
    const layout = manifest.layout?.[level];
    const grid = Math.ceil(level / TILE_SIZE);
    return layout?.faceSize === level
      && layout.tileSize === TILE_SIZE
      && layout.quality === DETAIL_QUALITY
      && JSON.stringify(layout.grid) === JSON.stringify([grid, grid]);
  });
  const manifestLevels = Object.keys(manifest.layout || {})
    .filter((key) => key !== 'base' && key !== 'tileFilename')
    .map(Number)
    .sort((left, right) => left - right);
  if (manifest.schemaVersion !== 3
    || typeof manifest.assetVersion !== 'string'
    || !manifest.assetVersion.trim()
    || (expectedAssetVersion !== null && manifest.assetVersion !== expectedAssetVersion)
    || manifest.capture?.sourceFaceSize !== SOURCE_SIZE
    || manifest.layout?.base?.faceSize !== BASE_SIZE
    || manifest.layout.base.tileSize !== BASE_SIZE
    || JSON.stringify(manifest.layout.base.grid) !== '[1,1]'
    || !refinementLayoutsAreValid
    || JSON.stringify(manifestLevels) !== JSON.stringify(REFINEMENT_LEVELS)
    || manifest.layout.tileFilename !== '<row>-<column>.webp') {
    throw new Error('Manifest capture or layout metadata is incorrect.');
  }
  const records = manifest.nodes.flatMap((node) => node.files);
  const expectedCount = expectedNodes.length * FACES.length * ASSETS_PER_FACE;
  if (records.length !== expectedCount) throw new Error(`Manifest has ${records.length} assets; expected ${expectedCount}.`);
  let bytes = 0;
  for (const node of manifest.nodes) {
    const expectedAssets = expectedNodeAssets(node.id);
    if (node.faceCount !== FACES.length || node.assetFileCount !== expectedAssets.size || node.files.length !== expectedAssets.size) {
      throw new Error(`${node.id} has incorrect face or asset counts.`);
    }
    let baseBytes = 0;
    let refinementBytes = 0;
    const refinementBytesByLevel = Object.fromEntries(REFINEMENT_LEVELS.map((level) => [level, 0]));
    for (const record of node.files) {
      const expected = expectedAssets.get(record.path);
      if (!expected || ['level', 'face', 'row', 'column', 'width', 'height'].some((field) => record[field] !== expected[field])) {
        throw new Error(`Unexpected asset layout: ${record.path}`);
      }
      expectedAssets.delete(record.path);
      const file = path.resolve(directory, record.path);
      if (!file.startsWith(`${directory}${path.sep}`)) throw new Error(`Unsafe manifest path: ${record.path}`);
      const buffer = await readFile(file);
      const dimensions = webpDimensions(buffer);
      if (buffer.length !== record.bytes || sha256(buffer) !== record.sha256 || dimensions[0] !== record.width || dimensions[1] !== record.height) {
        throw new Error(`Verification failed: ${record.path}`);
      }
      if (record.level === 'base') baseBytes += buffer.length;
      else {
        refinementBytes += buffer.length;
        refinementBytesByLevel[record.level] += buffer.length;
      }
      bytes += buffer.length;
    }
    if (expectedAssets.size) throw new Error(`${node.id} is missing ${expectedAssets.size} expected assets.`);
    if (node.baseBytes !== baseBytes
      || node.refinementBytes !== refinementBytes
      || JSON.stringify(node.refinementBytesByLevel) !== JSON.stringify(refinementBytesByLevel)
      || node.assetBytes !== baseBytes + refinementBytes) {
      throw new Error(`${node.id} byte totals are incorrect.`);
    }
  }
  const actual = await listWebps(directory);
  const listed = records.map((record) => record.path).sort();
  if (JSON.stringify(actual) !== JSON.stringify(listed)) throw new Error('Panorama directory has missing or unexpected WebP files.');
  if (manifest.totals.nodeCount !== expectedNodes.length
    || manifest.totals.capturedFaceCount !== expectedNodes.length * FACES.length
    || manifest.totals.assetFileCount !== records.length
    || manifest.totals.assetBytes !== bytes) throw new Error('Manifest totals are incorrect.');
  console.log(`Verified ${manifest.totals.nodeCount} nodes, ${records.length} WebPs, ${bytes} bytes.`);
  console.log(`Recorded toolchain: ${manifest.toolchain.chrome}; ${manifest.toolchain.imageMagick}; cwebp ${manifest.toolchain.cwebp}`);
}

async function loadIncrementalBaseline() {
  try {
    const manifest = JSON.parse(await readFile(path.join(OUTPUT_DIR, 'manifest.json'), 'utf8'));
    const existingVersion = typeof manifest.assetVersion === 'string' && manifest.assetVersion.trim()
      ? manifest.assetVersion
      : null;
    if (!existingVersion) throw new Error('Existing manifest has no asset version.');
    await verify(OUTPUT_DIR, { expectedAssetVersion: existingVersion });
    return manifest;
  } catch (error) {
    throw new Error(`Incremental generation requires a complete, compatible panorama set. Run once without --nodes. ${error.message}`);
  }
}

async function copyPreservedNodes(buildDir, manifest, selectedNodes) {
  const selected = new Set(selectedNodes);
  const preserved = new Map();
  for (const node of manifest.nodes) {
    if (selected.has(node.id)) continue;
    for (const record of node.files) {
      const source = path.resolve(OUTPUT_DIR, record.path);
      const destination = path.resolve(buildDir, record.path);
      if (!source.startsWith(`${OUTPUT_DIR}${path.sep}`) || !destination.startsWith(`${buildDir}${path.sep}`)) {
        throw new Error(`Unsafe preserved asset path: ${record.path}`);
      }
      await mkdir(path.dirname(destination), { recursive: true });
      await copyFile(source, destination);
    }
    preserved.set(node.id, node);
  }
  return preserved;
}

async function removeMarked(directory, allowedParent, prefix) {
  if (path.dirname(directory) !== allowedParent || !path.basename(directory).startsWith(prefix)) throw new Error(`Refusing unsafe cleanup: ${directory}`);
  let item;
  try { item = await lstat(directory); } catch (error) { if (error.code === 'ENOENT') return; throw error; }
  if (!item.isDirectory() || item.isSymbolicLink()) throw new Error(`Refusing unsafe cleanup: ${directory}`);
  await rm(directory, { recursive: true });
}

async function replaceOutput(buildDir) {
  let current;
  try { current = await lstat(OUTPUT_DIR); } catch (error) { if (error.code !== 'ENOENT') throw error; }
  if (!current) { await rename(buildDir, OUTPUT_DIR); return; }
  if (!current.isDirectory() || current.isSymbolicLink()) throw new Error(`Refusing to replace non-directory ${OUTPUT_DIR}.`);
  if ((await readFile(path.join(OUTPUT_DIR, MARKER), 'utf8').catch(() => '')) !== MARKER_TEXT) {
    throw new Error(`Refusing to replace unmarked directory ${OUTPUT_DIR}.`);
  }
  const backup = path.join(MOBILE_DIR, `.panoramas-backup-${process.pid}`);
  await rename(OUTPUT_DIR, backup);
  try { await rename(buildDir, OUTPUT_DIR); }
  catch (error) { await rename(backup, OUTPUT_DIR); throw error; }
  await removeMarked(backup, MOBILE_DIR, '.panoramas-backup-');
}

async function stopChrome(chrome) {
  if (chrome.exitCode !== null) return;
  chrome.kill('SIGTERM');
  await Promise.race([
    new Promise((resolve) => chrome.once('exit', resolve)),
    new Promise((resolve) => setTimeout(resolve, 3_000))
  ]);
  if (chrome.exitCode === null) chrome.kill('SIGKILL');
}

async function generate(options) {
  const selectedNodes = options.nodes || EXPECTED_NODES;
  const incremental = options.nodes !== null && selectedNodes.length < EXPECTED_NODES.length;
  const existingManifest = incremental ? await loadIncrementalBaseline() : null;
  const [chromeTool, magickTool, cwebpTool] = await Promise.all([
    findChrome(), findCommand('MAGICK_PATH', ['magick', 'convert'], ['-version']),
    findCommand('CWEBP_PATH', ['cwebp'], ['-version'])
  ]);
  console.log(`Chrome: ${chromeTool.version}`);
  console.log(`ImageMagick: ${magickTool.version}`);
  console.log(`cwebp: ${cwebpTool.version}`);
  const buildDir = await mkdtemp(path.join(MOBILE_DIR, '.panoramas-build-'));
  const workDir = await mkdtemp(path.join(tmpdir(), 'gallery-panorama-work-'));
  const chromeProfile = await mkdtemp(path.join(tmpdir(), 'gallery-panorama-chrome-'));
  let manifestNodesById = new Map();
  let server;
  let browser;
  let client;
  try {
    await writeFile(path.join(buildDir, MARKER), MARKER_TEXT);
    if (existingManifest) {
      manifestNodesById = await copyPreservedNodes(buildDir, existingManifest, selectedNodes);
      console.log(`Incremental build: rebuilding ${selectedNodes.length} node(s) and preserving ${manifestNodesById.size}.`);
    }
    server = await startStaticServer();
    browser = await launchChrome(chromeTool.command, chromeProfile);
    client = await CdpClient.connect(browser.pageWs);
    const failures = installFailureTracking(client);
    await Promise.all([
      client.send('Page.enable'), client.send('Runtime.enable'), client.send('Network.enable'), client.send('Log.enable')
    ]);
    await client.send('Network.setCacheDisabled', { cacheDisabled: true });
    await client.send('Network.setBypassServiceWorker', { bypass: true });
    await client.send('Emulation.setDeviceMetricsOverride', {
      width: 1280, height: 720, deviceScaleFactor: 1, mobile: false,
      screenWidth: 1280, screenHeight: 720
    });
    await client.send('Emulation.setLocaleOverride', { locale: 'en-US' });
    await client.send('Emulation.setTimezoneOverride', { timezoneId: 'UTC' });
    const loaded = client.waitFor('Page.loadEventFired');
    const navigation = await client.send('Page.navigate', { url: `${server.origin}/?gallery=full&gallery-capture=1` });
    if (navigation.errorText) throw new Error(`Navigation failed: ${navigation.errorText}`);
    await loaded;
    const captureInfo = await evaluate(client, `(async () => {
      const timeout = new Promise((_, reject) => setTimeout(() => reject(new Error('Capture API timed out')), 180000));
      const api = await Promise.race([(async () => {
        while (!window.__galleryCapture) await new Promise((resolve) => setTimeout(resolve, 25));
        return window.__galleryCapture;
      })(), timeout]);
      await Promise.race([api.ready, timeout]);
      if (!Array.isArray(api.nodeIds) || typeof api.captureFace !== 'function') throw new Error('Invalid capture API');
      return { nodeIds: [...api.nodeIds], diagnostics: api.diagnostics };
    })()`);
    if (new Set(captureInfo.nodeIds).size !== EXPECTED_NODES.length || EXPECTED_NODES.some((id) => !captureInfo.nodeIds.includes(id))) {
      throw new Error(`Capture API nodeIds do not match the expected ${EXPECTED_NODES.length} nodes.`);
    }
    confirmCaptureScene(failures, captureInfo.diagnostics);
    console.log(`Capture readiness: ${JSON.stringify(captureInfo.diagnostics)}`);
    assertClean(failures, server);
    let captureIndex = 0;
    for (const nodeId of selectedNodes) {
      const files = [];
      for (const face of FACES) {
        captureIndex += 1;
        console.log(`[${captureIndex}/${selectedNodes.length * FACES.length}] ${nodeId}/${face}`);
        const dataUrl = await evaluate(client, `window.__galleryCapture.captureFace(${JSON.stringify(nodeId)}, ${JSON.stringify(face)}, ${SOURCE_SIZE})`);
        const png = decodePng(dataUrl, nodeId, face);
        files.push(...await buildFace({
          nodeId, face, png, buildDir, workDir,
          magick: magickTool.command, cwebp: cwebpTool.command
        }));
        assertClean(failures, server);
      }
      const baseBytes = files.filter((file) => file.level === 'base').reduce((sum, file) => sum + file.bytes, 0);
      const refinementBytes = files.filter((file) => file.level !== 'base').reduce((sum, file) => sum + file.bytes, 0);
      const refinementBytesByLevel = Object.fromEntries(REFINEMENT_LEVELS.map((level) => [
        level,
        files.filter((file) => file.level === String(level)).reduce((sum, file) => sum + file.bytes, 0)
      ]));
      if (baseBytes > MAX_BASE_BYTES) throw new Error(`${nodeId} base is ${baseBytes} bytes; budget is ${MAX_BASE_BYTES}.`);
      for (const [level, levelBytes] of Object.entries(refinementBytesByLevel)) {
        if (levelBytes > MAX_REFINEMENT_BYTES) {
          throw new Error(`${nodeId} ${level}px refinement is ${levelBytes} bytes; budget is ${MAX_REFINEMENT_BYTES}.`);
        }
      }
      manifestNodesById.set(nodeId, {
        id: nodeId,
        faceCount: FACES.length,
        assetFileCount: files.length,
        baseBytes,
        refinementBytes,
        refinementBytesByLevel,
        assetBytes: baseBytes + refinementBytes,
        files
      });
    }
    const manifestNodes = EXPECTED_NODES.map((nodeId) => manifestNodesById.get(nodeId));
    if (manifestNodes.some((node) => !node)) throw new Error('Complete manifest could not be assembled.');
    await new Promise((resolve) => setTimeout(resolve, 250));
    assertClean(failures, server);
    const assetFileCount = manifestNodes.reduce((sum, node) => sum + node.assetFileCount, 0);
    const assetBytes = manifestNodes.reduce((sum, node) => sum + node.assetBytes, 0);
    const manifest = {
      schemaVersion: 3,
      assetVersion: ASSET_VERSION,
      generatedBy: 'gallery/mobile/scripts/generate-panoramas.mjs',
      capture: { url: '/?gallery=full&gallery-capture=1', sourceFaceSize: SOURCE_SIZE, faces: FACES },
      layout: {
        base: { faceSize: BASE_SIZE, tileSize: BASE_SIZE, grid: [1, 1], quality: BASE_QUALITY },
        ...Object.fromEntries(REFINEMENT_LEVELS.map((level) => {
          const grid = Math.ceil(level / TILE_SIZE);
          return [level, {
            faceSize: level,
            tileSize: TILE_SIZE,
            grid: [grid, grid],
            quality: DETAIL_QUALITY
          }];
        })),
        tileFilename: '<row>-<column>.webp'
      },
      budgets: {
        maxBaseBytesPerNode: MAX_BASE_BYTES,
        maxRefinementBytesPerLevelPerNode: MAX_REFINEMENT_BYTES
      },
      toolchain: { node: process.version, chrome: chromeTool.version, imageMagick: magickTool.version, cwebp: cwebpTool.version },
      totals: { nodeCount: manifestNodes.length, capturedFaceCount: manifestNodes.length * FACES.length, assetFileCount, assetBytes },
      nodes: manifestNodes
    };
    await writeFile(path.join(buildDir, 'manifest.json'), `${JSON.stringify(manifest, null, 2)}\n`);
    await verify(buildDir);
    client.close();
    client = null;
    await stopChrome(browser.chrome);
    browser = null;
    await server.close();
    server = null;
    await replaceOutput(buildDir);
    console.log(`${incremental ? 'Updated' : 'Wrote'} ${OUTPUT_DIR}`);
  } finally {
    if (client) client.close();
    if (browser) await stopChrome(browser.chrome);
    if (server) await server.close().catch(() => {});
    await removeMarked(buildDir, MOBILE_DIR, '.panoramas-build-').catch(() => {});
    await removeMarked(workDir, tmpdir(), 'gallery-panorama-work-').catch(() => {});
    await removeMarked(chromeProfile, tmpdir(), 'gallery-panorama-chrome-').catch(() => {});
  }
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  if (options.help) { console.log(usage()); return; }
  if (options.verify) { await verify(OUTPUT_DIR); return; }
  await generate(options);
}

main().catch((error) => {
  console.error(error.stack || error.message);
  process.exitCode = 1;
});
